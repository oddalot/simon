      this.inputAGo = false;
      let i = 0;
      const displaySeqTimer = setInterval(() => {
        this.clearDisplay();
        if (i > this.currentLength) {
          clearInterval(displaySeqTimer);
          this.awaitInput();
        }
        if (i % 0.5 !== 0.5) {
          this.turnOnColor(this.mainSeq[i]);
        }
        i += 0.5;
      }, 600);
    },
