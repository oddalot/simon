export default {
  name: 'hello',
  data() {
    return {
      greenOn: false,
      redOn: false,
      blueOn: false,
      yellowOn: false,
      msg: 'Welcome to Your Vue.js App',
      mainSeq: [],
      currentLength: 0,
      currentCheckLength: 0,
      inputAGo: false,
      lostGame: false,
      wonGame: false,
      colorMap: {
        green: 0,
        red: 1,
        blue: 2,
        yellow: 3,
      },
    };
  },
  methods: {
    clickOn() {
      this.wonGame = false;
      this.lostGame = false;
      this.currentLength = 0;
      this.currentCheckLength = 0;
      this.createMainSeq(4);
      this.displayCurrentSeq();
    },
    incrementGame() {
      this.currentLength += 1;
      if (this.currentLength >= this.mainSeq.length) {
        this.winGame();
      } else {
        this.currentCheckLength = 0;
        this.displayCurrentSeq();
      }
    },
    winGame() {
      this.wonGame = true;
      this.currentLength = 0;
      this.createMainSeq(4);
    },
    loseGame() {
      this.lostGame = true;
      setTimeout(() => {
        this.currentCheckLength = 0;
        this.lostGame = false;
        this.clearDisplay();
        this.displayCurrentSeq();
      }, 1500);
    },
    clickColor(color) {
      this.turnOnColor(this.colorMap[color]);
      setTimeout(() => {
        this.clearDisplay();
      }, 250);
      if (this.inputAGo) {
        if (this.mainSeq[this.currentCheckLength] === this.colorMap[color]) {
          this.currentCheckLength += 1;
          if (this.currentCheckLength > this.currentLength) {
            this.incrementGame();
          }
        } else {
          this.loseGame();
        }
      }
    },
    clearDisplay() {
      this.greenOn = false;
      this.redOn = false;
      this.blueOn = false;
      this.yellowOn = false;
    },
    turnOnColor(color) {
      switch (color) {
        case 0:
          this.greenOn = true;
          break;
        case 1:
          this.redOn = true;
          break;
        case 2:
          this.blueOn = true;
          break;
        case 3:
          this.yellowOn = true;
          break;
        default:
          break;
      }
    },
    createMainSeq(gameLength) {
      this.mainSeq = [];
      for (let i = 0; i < gameLength; i += 1) {
        this.mainSeq.push(Math.floor(Math.random() * (4)));
      }
    },
    displayCurrentSeq() {
      this.inputAGo = false;
      let i = 0;
      const displaySeqTimer = setInterval(() => {
        this.clearDisplay();
        if (i > this.currentLength) {
          clearInterval(displaySeqTimer);
          this.awaitInput();
        }
        if (i % 0.5 !== 0.5) {
          this.turnOnColor(this.mainSeq[i]);
        }
        i += 0.5;
      }, 600);
    },
    awaitInput() {
      this.inputAGo = true;
    },
  },
};
